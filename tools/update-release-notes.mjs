#!/usr/bin/env node
// InkFrame release-notes generator
// -----------------------------------------------------------------------------
// Builds RELEASE_NOTES.md from web/metadata.json plus either:
//   * release-notes/<version>.md, when present; or
//   * CHANGELOG.md [Unreleased] as the normal fallback.

import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';
import { releaseChanges } from './release-notes-contract.mjs';

const here = dirname(fileURLToPath(import.meta.url));
const root = resolve(here, '..');
const args = new Set(process.argv.slice(2));
const CHECK = args.has('--check');

const read = p => readFileSync(resolve(root, p), 'utf8');
const normalize = s => s.replace(/\r\n/g, '\n').trimEnd() + '\n';

function generatedNotes() {
  const meta = JSON.parse(read('web/metadata.json'));
  const date = meta.releaseDate || new Date().toISOString().slice(0, 10);
  const name = meta.name || 'InkFrame Studio';
  const version = meta.version || 'dev';
  const desc = meta.description || 'Offline-first 2D drawing and frame-by-frame animation.';
  const changes = releaseChanges(root, version);

  return normalize(`# ${name} ${version} Release Notes

_Date: ${date}_

<!-- Generated by tools/update-release-notes.mjs. Do not edit this file manually. -->

${desc}

## Before you install or test

1. Open **Studio** in the app.
2. Tap **Backup archive**.
3. Save the downloaded \`.inkframe\` somewhere outside browser/app storage.
4. Then install/test the latest APK or browser build.

See \`RELEASE_CHECKLIST.md\` for the full tablet/browser/APK smoke-test flow.

## What's changed

${changes}

## Minimum smoke test

- Start overlay appears on a fresh/no-recovery session.
- **Import archive** from Start restores a \`.inkframe\`.
- Gallery templates and custom canvas creation work.
- V2 Ink and Eraser draw with Ribbon, Guarded width, and Strict contact defaults.
- Switching to Original produces an independent fallback stroke path.
- Separate strokes and fast uninterrupted strokes never create a long connecting bridge.
- Brush Lab texture, brush preset save/use/export/import/delete work.
- Two-finger pan and pinch zoom remain anchored, do not rotate the canvas, and do not leave an accidental touch mark.
- PNG/GIF/video export paths report success or a clear unsupported message.
- Installed APK launches offline and can export media/archive files.

## Known limitations

- V2 currently owns S Pen Ink and Eraser; unsupported specialty brushes, touch, and mouse continue through the original engine.
- Native MotionEvent telemetry and raw trace export are debug-build diagnostics and are not retained by production releases.
- Browser/Android media support varies; video export may fall back or report unsupported depending on WebView/browser codecs.
- \`.inkframe\` archives are JSON with PNG data URLs, not a compressed ZIP package yet.
- Local autosave is device/browser storage; use \`.inkframe\` archive export for real backups.
`);
}

const outPath = resolve(root, 'RELEASE_NOTES.md');
const next = generatedNotes();

if (CHECK) {
  let current = '';
  try { current = normalize(read('RELEASE_NOTES.md')); } catch { current = ''; }
  if (current !== next) {
    console.error('❌ RELEASE_NOTES.md is out of date. Run: node tools/update-release-notes.mjs');
    process.exit(1);
  }
  console.log('✅ Release notes are current.');
} else {
  writeFileSync(outPath, next);
  console.log('✅ Wrote RELEASE_NOTES.md');
}
