// Generated Android viewport postprocessor: replaces the legacy canvas-only
// pinch recognizer with a testable two-finger pan/zoom runtime and host bridge.

const block=(...lines)=>lines.join('\n');

export function injectViewportGestures(html,replaceOnce){
  html=replaceOnce(html,
    block('<script src="canvas-shape.js"></script>','<!-- INKFRAME_BRUSH_V2_RUNTIME: generated into APK assets only -->'),
    block('<script src="canvas-shape.js"></script>','<script src="viewport-gestures.js"></script>','<!-- INKFRAME_BRUSH_V2_RUNTIME: generated into APK assets only -->'),
    'viewport gesture runtime script');

  html=replaceOnce(html,
    '  let cScale=1;                                   // display scale of the canvas element',
    '  let cScale=1, cPanX=0, cPanY=0;                 // display scale + viewport translation',
    'viewport pan state');

  html=replaceOnce(html,
    '  function clampCanvas(){ const f=fitScale(); cScale=Math.max(f*0.35, Math.min(cScale, f*2.2)); }',
    block(
      '  function clampCanvas(){ const f=fitScale(); cScale=Math.max(f*0.35, Math.min(cScale, f*2.2)); }',
      '  function viewportState(){',
      '    const fit=fitScale();',
      '    return Object.freeze({',
      '      scale:cScale, panX:cPanX, panY:cPanY, fitScale:fit,',
      '      minScale:fit*0.35, maxScale:fit*2.2,',
      '      viewportWidth:window.innerWidth, viewportHeight:window.innerHeight,',
      '      centerX:window.innerWidth/2, centerY:window.innerHeight/2,',
      '      canvasWidth:W, canvasHeight:H',
      '    });',
      '  }',
      '  function clampCanvasPan(){',
      '    const overscan=Math.max(64,Math.min(112,Math.min(window.innerWidth,window.innerHeight)*0.12));',
      '    const maxX=Math.max(0,(W*cScale-window.innerWidth)/2+overscan);',
      '    const maxY=Math.max(0,(H*cScale-window.innerHeight)/2+overscan);',
      '    cPanX=Math.max(-maxX,Math.min(maxX,Number(cPanX)||0));',
      '    cPanY=Math.max(-maxY,Math.min(maxY,Number(cPanY)||0));',
      '  }'),
    'viewport scale and pan limits');

  html=replaceOnce(html,
    block(
      '  function applyCanvas(){',
      "    canvas.style.width=Math.round(W*cScale)+'px'; canvas.style.height=Math.round(H*cScale)+'px';",
      "    if(timelineReady && typeof buildFrameBoard==='function') requestAnimationFrame(buildFrameBoard);",
      '    if(window.InkFrameCanvasShape) requestAnimationFrame(()=>window.InkFrameCanvasShape.sync());',
      '  }'),
    block(
      '  function applyCanvas(){',
      '    clampCanvasPan();',
      "    canvas.style.width=Math.round(W*cScale)+'px'; canvas.style.height=Math.round(H*cScale)+'px';",
      "    frameGlass.style.transform=`translate3d(${Math.round(cPanX)}px,${Math.round(cPanY)}px,0)`;",
      "    frameGlass.style.transformOrigin='50% 50%';",
      "    if(timelineReady && typeof buildFrameBoard==='function') requestAnimationFrame(buildFrameBoard);",
      '    if(window.InkFrameCanvasShape) requestAnimationFrame(()=>window.InkFrameCanvasShape.sync());',
      "    if(typeof window.CustomEvent==='function') window.dispatchEvent(new CustomEvent('inkframe:viewportchange',{detail:viewportState()}));",
      '  }'),
    'viewport transform application');

  html=replaceOnce(html,
    '  cScale=fitScale(); applyCanvas();',
    block(
      '  window.InkFrameViewportEnvironment=()=>({',
      '    stage,canvas,frameGlass,',
      '    getState:()=>viewportState(),',
      '    setState(next){',
      '      const value=next||{};',
      '      if(Number.isFinite(Number(value.scale)))cScale=Number(value.scale);',
      '      clampCanvas();',
      '      if(Number.isFinite(Number(value.panX)))cPanX=Number(value.panX);',
      '      if(Number.isFinite(Number(value.panY)))cPanY=Number(value.panY);',
      '      clampCanvasPan();applyCanvas();startWires();return viewportState();',
      '    },',
      '    fit(){cScale=fitScale();cPanX=0;cPanY=0;applyCanvas();startWires();return viewportState();},',
      '    center(){cPanX=0;cPanY=0;applyCanvas();startWires();return viewportState();},',
      '    canGesture(){',
      '      const adapter=window.InkFrameBrushV2Adapter;',
      "      if(drawing&&drawPidType==='pen')return false;",
      '      return !(adapter&&adapter.isActive&&adapter.isActive());',
      '    },',
      '    cancelTouchStroke(){',
      "      if(!drawing||drawPidType==='pen')return false;",
      '      const owner=drawPid;drawing=false;drawPid=null;hideLens();clearTimeout(qsTimer);clearPredicted();',
      '      try{if(owner!=null&&canvas.hasPointerCapture&&canvas.hasPointerCapture(owner))canvas.releasePointerCapture(owner);}catch(_){}',
      '      if(pend){restoreSnap(pend);pend=null;render();}',
      '      endBarrelEraser();qsActive=false;qsShape=null;strokePath=[];',
      '      return true;',
      '    },',
      '    undo:()=>doUndo(),redo:()=>doRedo(),flash:message=>flash(message)',
      '  });',
      '  cScale=fitScale(); applyCanvas();'),
    'viewport host bridge');

  const legacy=block(
    '  // pinch on the canvas → scale the canvas (NOT a UI zoom). Two-finger only; one finger draws.',
    '  // Multi-touch on the canvas:',
    '  //   • 2 fingers drag  → pinch-scale the canvas',
    '  //   • 2-finger tap    → Undo      • 3-finger tap → Redo   (quick, little movement)',
    '  const active=new Map(); let pinchBase=0, pinchScale0=1;',
    '  let gStart=0, gMaxN=0, gMoved=false, gStartPts=[];',
    '  function gAvgMove(){ // largest movement of any finger since gesture start',
    '    let m=0, i=0; for(const e of active.values()){ const s=gStartPts[i++]; if(s) m=Math.max(m,Math.hypot(e.clientX-s.x,e.clientY-s.y)); } return m; }',
    "  canvas.addEventListener('pointerdown',e=>{",
    '    active.set(e.pointerId,e);',
    '    if(active.size===1){ gStart=performance.now(); gMoved=false; }',
    '    gMaxN=Math.max(gMaxN,active.size);',
    '    gStartPts=[...active.values()].map(p=>({x:p.clientX,y:p.clientY}));',
    '    // A 2nd finger cancels a TOUCH stroke (intending a gesture). But if a PEN owns the',
    '    // stroke, an extra touch is a resting palm → keep drawing, ignore the touch entirely.',
    "    if(active.size>=2 && drawing && drawPidType!=='pen'){",
    '      drawing=false; drawPid=null; clearPredicted();',
    '      if(pend){ restoreSnap(pend); pend=null; render(); }',
    '    }',
    '  }, true);',
    "  canvas.addEventListener('pointermove',e=>{",
    '    if(active.has(e.pointerId)) active.set(e.pointerId,e);',
    "    if(active.size>=2 && !(drawing && drawPidType==='pen')){   // don't pinch while a pen stroke is live (palm resting)",
    '      if(gAvgMove()>10) gMoved=true;',
    '      const [p,q]=[...active.values()]; const dist=Math.hypot(p.clientX-q.clientX,p.clientY-q.clientY);',
    '      if(gMoved){ if(!pinchBase){ pinchBase=dist; pinchScale0=cScale; }',
    '        else { cScale=Math.max(0.18, pinchScale0*(dist/pinchBase)); clampCanvas(); applyCanvas(); startWires(); } }',
    '    }',
    '  }, true);',
    '  const endPtr=e=>{',
    '    const wasN=active.size;',
    '    active.delete(e.pointerId);',
    '    ' + "if(active.size===0){                            // whole gesture lifted",
    '      const quick=(performance.now()-gStart)<420;',
    '      if(quick && !gMoved){',
    '        if(gMaxN===2) doUndo();',
    '        else if(gMaxN>=3) doRedo();',
    '      }',
    '      pinchBase=0; gMaxN=0; gMoved=false;',
    '    } else if(active.size<2){ pinchBase=0; }',
    '  };',
    "  canvas.addEventListener('pointerup',endPtr,true); canvas.addEventListener('pointercancel',endPtr,true);");
  html=replaceOnce(html,legacy,
    block('  // Multi-touch viewport gestures are owned by viewport-gestures.js.','  // The generated bridge above keeps drawing rollback and viewport state private.'),
    'legacy pinch recognizer removal');

  return html;
}
